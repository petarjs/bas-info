export default class TextUtils {

  static removeDiacritics(str) {
    const chars = str.split('');
    let i = chars.length - 1;
    let alter = false;
    let ch;
    for (; i >= 0; i--) {
      ch = chars[i];
      if (diacritics.hasOwnProperty(ch)) {
        chars[i] = diacritics[ch];
        alter = true;
      }
    }
    if (alter) {
      return chars.join('');
    }
    return str;
  }
}
